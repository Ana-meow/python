# Определение суммы чисел n + nn + nnn
n = int(input("Введите число от 1 до 10: "))
# var - введённое число, как символ
var = str(n)
# var2 - формирование второго числа, как символа
var2 = var + var
# var3 - формирование третьего числа, как символа
var3 = var + var + var
# result - арифметическая сумма чисел
result = n + int(var2) + int(var3)
print(f"Результат: {result}.")












