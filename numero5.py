# определение номера дня, на который общий результат спортсмена составит не менее b километров
# a - километры в первый день
a = int(input("Введите значение a: "))
# b - максимальные километры
b = int(input("Введите значение b: "))
# n - номер дня
n = 1
while a <= b:
    n = n + 1
    # ежедневное увеличение дистаниции на 10%
    a = a + a / 10
print(f"Результат будет достигнут на {n}-й день.")
