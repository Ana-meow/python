# Определение максимальной цифры в числе
n = int(input("Введите целое положительное число:"))
# выделение цифры из числа, как остатка от деления на 10
m = n % 10
# присвоение переменной целой части от деления на 10
n = n // 10
# сравнение след. цифры, как остатка от деления на 10, с последней цифрой
while n > 0:
    if n % 10 > m:
        m = n % 10
    n = n // 10
print(f"Самая большая цифра в этом цисле: {m}")
